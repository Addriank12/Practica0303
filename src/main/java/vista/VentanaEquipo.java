    private void updateTable()
    {
        var header = new String[] {"Nombre", "N. Jugadores", "confederacion", "Propietario","gastos"};
        var data = new Object[this.controladorEquipo.listar().size()][5];
        
        for(int i = 0; i<this.controladorEquipo.listar().size();i++)
        {
            data[i][0]=this.controladorEquipo.listar().get(i).getNombre();
            data[i][1]=this.controladorEquipo.listar().get(i).getnJugadores();
            data[i][2]=this.controladorEquipo.listar().get(i).getConfederacion();
            data[i][3]=this.controladorEquipo.listar().get(i).getPropietario();
            data[i][4]=this.controladorEquipo.listar().get(i).getGastos();
        }
        var dataModel = new DefaultTableModel(data,header);
        this.jTable1.setModel(dataModel);                
    }

